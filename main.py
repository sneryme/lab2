def add(a, b):
    """Функция складывает два числа."""
    return a + b


def multiply(a, b):
    """Функция умножает два числа."""
    return a * b
def subtract(a, b):
    """Вычитает b из a."""
    return a - b


if __name__ == "__main__":
    print("Сумма:", add(2, 3))
    print("Произведение:", multiply(2, 3))
